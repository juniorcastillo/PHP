				<p><b>AIDA64</b>de FinalWire Ltd. (antes Everest de Lavalys y AIDA32) es un programa de computador gratuito
				que proporciona información de los componentes hardware del mismo. Escrito por Tamas Miklos este programa
				funciona sobre Microsoft Windows. Toda la información detallada que nos muestra se puede grabar en formatos
				HTML, CSV o XML.</p>
				
				
				
				<p>Es una aplicación informática, de código cerrado, que sirve como herramienta de diagnóstico del 
				sistema. Ofrece posibilidades de multilenguaje y está diseñado para ejecutarse sobre sistemas operativos,
				tanto de 32 bits como de 64 bits, basados en la familia de Microsoft Windows; siendo compatible con todas
				las versiones desde la 95 hasta la 7, incluyendo las versiones enfocadas hacia la línea de servidores. 
				En sus diferentes paneles, muestra información sobre todos los dispositivos de hardware, programas instalados
				y rendimiento de los componentes, para lo cual realiza un extenso y detallado análisis del sistema, mostrando
				prácticamente todos los aspectos del mismo referentes a hardware, software, configuración de red, así como 
				diversas pruebas de rendimiento o benchmark.</p>
				
				<h2 id="versiones">Versiones de AIDA64</h2>
				
				<p>la empresa creadora de la aplicación, dispone de dos versiones. Ambas son comerciales, aunque es posible
				descargar de la Web oficial de AIDA64 versiones freeware que a pesar de no incluir todas las funciones 
				permiten efectuar los diferentes test, medir el desempeño, identificar el hardware y actualizar los drivers
				del sistema. Son éstas:<p>
				<ul>
					<li><b>AIDA64 Extreme Edition:</b>es la utilidad de diagnóstico y bechnmarking para usuarios domésticos.</li>
					<li><b>AIDA64 Business Edition:</b> es la solución enfocada a la administración de redes para pequeñas y 
					medianas empresas.</li>
					<li><b>AIDA64 Engineer:</b>AIDA64 Engineer es una herramienta de información del sistema de vanguardia para los técnicos de TI, que no sólo proporciona una información muy detallada sobre el hardware y el software instalado, sino que también ayuda a los usuarios a diagnosticar problemas y ofrece puntos de referencia para medir el rendimiento de ambas estaciones de trabajo y servidores.</li>
					
					<li><b>AIDA64 Network Audit:</b>Nuestra solución de auditoría de red dedicada para las empresas que lo apoya la toma de decisiones con estadísticas esenciales, y ayuda a las empresas a reducir sus costes de TI.</li>
				</ul>
				
				<h2 id="caracteristicas">Características generales</h2>
				 <div> AIDA64 constituye una de las suite informáticas más completas encargadas de realizar un extenso y 
				  detallado análisis del sistema, el cual muestra en una estructura de árbol. Incluye módulos de
				  identificación del hardware de la computadora, módulos de diagnóstico, un conjunto bastante completo
				  de pruebas de rendimiento o benchmarks e inclusive, si es posible, facilita los enlaces o links para
				  la descarga de drivers.</div>
				<h2 id="pruebas">Pruebas de rendimiento o benchmark</h2>
	    

			   <div>La herramienta incluye diversos bancos de pruebas especializados enfocados en las arquitecturas y sistemas de 64 bits para someter a examen la capacidad del procesador, a través de las conocidas instrucciones de identificación de la CPU, dentro de las que también incorpora cálculos de punto flotante, de compresión y memoria; proporciona chequeos de la estabilidad del sistema con herramientas para detección y verificación de nuevas tecnologías como las unidades de esta sólido SSD; y un bastante completo diagnóstico al monitor con opciones especiales para aquellos que emplean pantallas LCD.</div>
			   
		
				  
				
